package dayone;

public class CharFormate {

	public static void main(String args[]) {
		
		char ch='v';
		System.out.println(ch);   //assigning single char
		
		char ch1=77;
		System.out.println(ch1);
		  
		char var1= '\u00A1';
		System.out.println(var1); //assigning unicode to char
		
		char var2='\u20AD';
		System.out.println(var2);// unicode representation
		
		int a='N';
		System.out.println(a);
	}
}
